package Demo;

public class Department {
	private String name;
	private String code;
	private Demo.Manager manager;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Demo.Manager getManager() {
		return manager;
	}
	public void setManager(Demo.Manager manager) {
		this.manager = manager;
	}
	
	
}
